package edu.ucalgary.oop;

/**
 * The AnimalType enum represents a set of predefined animal types.
 * This enumeration is intended to be used in the context of the project
 * for modeling and categorizing different animals within the system.
 *
 * The enum currently supports the following animal types:
 * - COYOTE
 * - FOX
 * - PORCUPINE
 * - RACCOON
 * - BEAVER
 */
public enum AnimalType {
    // Enumerated values representing different animal types
    COYOTE, // Represents a Coyote
    FOX, // Represents a Fox
    PORCUPINE, // Represents a Porcupine
    RACCOON, // Represents a Raccoon
    BEAVER; // Represents a Beaver
}
