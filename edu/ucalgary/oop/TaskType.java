package edu.ucalgary.oop;

/**
 * Represents the different types of tasks that can be assigned.
 * This enumeration contains the following types: MEDICAL, CLEANING, and
 * FEEDING.
 * 
 * @author Aarsh
 */
public enum TaskType {
    // Represents a task related to medical care or procedures
    MEDICAL,
    // Represents a task related to cleaning or maintenance
    CLEANING,
    // Represents a task related to feeding or providing nutrition
    FEEDING;
}
